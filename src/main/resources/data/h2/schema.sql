CREATE TABLE FABRICANTES(

	CODIGO				INTEGER				NOT NULL,
	NOMBRE				VARCHAR(25)			,
	
	PRIMARY KEY(CODIGO)
);

CREATE TABLE VIDEOJUEGOS(

	CODIGO				INTEGER				NOT NULL,
	TITULO				VARCHAR(30)			,
	FECHA_ALTA			DATE				,
	PRECIO				DOUBLE				,
	DESCATALOGADO		BOOLEAN				,
	TIPO_JUEGO			VARCHAR(11)			,
	CODIGO_FABRICANTE	INTEGER				,
	
	
	PRIMARY KEY(CODIGO),
	FOREIGN KEY (CODIGO_FABRICANTE) REFERENCES FABRICANTES (CODIGO)
	
);